    private void CancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelButtonActionPerformed
        while (!listTable.isEmpty()) {
            listTable.remove(0);
        }
        i = 1;
        tongTien = 0;
        Tong = 0;
        giamGia = 0;
        Reset();
        setTimeField();
        updateTable();
    }//GEN-LAST:event_CancelButtonActionPerformed

